/*
11.1.1. Двое играют в такую игру: на столе лежит 20 спичек; играющие по очереди могут взять от 1 до 4 спичек; кто не может сделать хода (спичек не осталось) — проигрывает. Кто выигрывает при правильной игре?
Теория игр: выигрышные/проигрышные позиции (mod 5). При 20 спичках (20 % 5 = 0) — проигрыш для первого, выигрывает второй (оптимальная стратегия: оставлять кратное 5).
*/

/**
 * Определяет победителя в игре с N спичками
 * @param sticks - начальное количество спичек (по умолчанию 20)
 * @param maxTake - максимальное количество спичек, которое можно взять за ход (по умолчанию 4)
 * @returns строку с указанием победителя
 */
function determineGameWinner(sticks: number = 20, maxTake: number = 4): string {
  // Если количество спичек кратно (maxTake + 1), то выигрывает второй игрок
  if (sticks % (maxTake + 1) === 0) {
    return "Второй игрок выигрывает";
  }
  return "Первый игрок выигрывает";
}
